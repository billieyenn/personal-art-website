// Function to find the point closest to a target point
const findClosestPoint = (pointsArray, targetPoint) => {
  if (pointsArray.length === 0) {
    return null // Return null if the array is empty
  }

  // Using reduce to find the closest point
  const closestPoint = pointsArray.reduce((closest, current) => {
    const distanceToTarget = current.dist(targetPoint)
    const closestDistance = closest.dist(targetPoint)
    return distanceToTarget < closestDistance ? current : closest
  })

  return closestPoint
}

