// Function to perform scaling transformation on the points
const scalePoints = (points, scaleX, scaleY) => {
  const copyArray = [...points]

  copyArray.forEach((point) => {
    point.x *= scaleX
    point.y *= scaleY 
  })
  
  return copyArray
}

// Function to perform translation transformation on the points
const translatePoints = (points, translationX, translationY) => {
  const copyArray = [...points]

  copyArray.forEach((point) => {
    point.x += translationX
    point.y += translationY 
  })
  
  return copyArray
}

// Function to calculate the center point of the given points
const calculateCenter = (points) => {
  const totalPoints = points.length
  const sumX = points.reduce((acc, [x]) => acc + x, 0)
  const sumY = points.reduce((acc, [, y]) => acc + y, 0)
  return [sumX / totalPoints, sumY / totalPoints]
}

// Function to perform rotation around the center of the points
const rotatePointsAroundCenter = (p, vectors, angle) => {
  const points = [...vectors].map((vector) => [vector.x, vector.y])

  const center = calculateCenter(points)

  // Translate the points so that the center becomes the new origin
  const translatedPoints = points.map(([x, y]) => [x - center[0], y - center[1]])

  // Perform the rotation around the new origin (center)
  const cosA = Math.cos(angle)
  const sinA = Math.sin(angle)
  const rotatedPoints = translatedPoints.map(([x, y]) => [
    x * cosA - y * sinA,
    x * sinA + y * cosA
  ])

  // Translate the points back to their original position
  const finalPoints = rotatedPoints.map(([x, y]) => [x + center[0], y + center[1]])

  return finalPoints.map(([x, y]) => p.createVector(x, y))
}

